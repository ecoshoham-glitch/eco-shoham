      if (finalData.schoolEmail && finalData.schoolEmail !== '—') params.set(FORM_FIELDS.schoolEmail, finalData.schoolEmail);
      const kitInfo = [finalData.kitType, finalData.kitSize].filter(v => v && v !== '—').join(' | ');
