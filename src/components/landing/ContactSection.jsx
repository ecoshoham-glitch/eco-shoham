function findFaqAnswer(question) {
  const q = question.toLowerCase();
  // Score each FAQ by longest matching keyword — prefer more specific matches
  let bestMatch = null;
  let bestLen = 0;
  for (const faq of KIT_FAQ) {
    for (const kw of faq.keywords) {
      if (q.includes(kw) && kw.length > bestLen) {
        bestMatch = faq;
        bestLen = kw.length;
      }
    }
  }
  return bestMatch;
}

